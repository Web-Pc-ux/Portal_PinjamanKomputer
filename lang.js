let currentLang = localStorage.getItem('language') || 'BM';

function initLanguage() {
    // Inject Toggle Button into DOM ONLY if not in dashboard (sidebar doesn't exist)
    if (!document.getElementById('sidebar')) {
        const btnHtml = `
            <button id="langToggleBtn" onclick="toggleLanguage()" style="
                position: fixed; 
                top: 20px; 
                right: 20px; 
                z-index: 9999; 
                background: rgba(255, 255, 255, 0.85); 
                backdrop-filter: blur(10px); 
                border: 1px solid #ccc; 
                padding: 8px 16px; 
                border-radius: 20px; 
                cursor: pointer; 
                font-weight: bold; 
                box-shadow: 0 4px 6px rgba(0,0,0,0.1); 
                color: #2a3eb1; 
                font-family: 'Poppins', sans-serif;
                display: flex;
                align-items: center;
                gap: 6px;
                transition: all 0.3s ease;
                width: auto;
                font-size: 14px;">
                <i class="fas fa-globe"></i> <span id="langText">${currentLang === 'BM' ? 'EN' : 'BM'}</span>
            </button>
        `;
        
        if(!document.getElementById('langToggleBtn')) {
            document.body.insertAdjacentHTML('beforeend', btnHtml);
        }
    }

    applyTranslations();
    updateToggleText();
}

function toggleLanguage() {
    currentLang = currentLang === 'BM' ? 'EN' : 'BM';
    localStorage.setItem('language', currentLang);
    applyTranslations();
    updateToggleText();
}

function updateToggleText() {
    const langText = document.getElementById('langText');
    if (langText) langText.innerText = currentLang === 'BM' ? 'EN' : 'BM';
    
    const langTextSidebar = document.getElementById('langTextSidebar');
    if (langTextSidebar) {
        langTextSidebar.innerText = currentLang === 'BM' ? 'Tukar Bahasa (EN)' : 'Change Language (BM)';
    }
}

function applyTranslations() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[key] && translations[key][currentLang]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (el.hasAttribute('placeholder')) {
                    el.setAttribute('placeholder', translations[key][currentLang]);
                }
            } else {
                el.innerHTML = translations[key][currentLang];
            }
        }
    });
}

// Run on load
document.addEventListener('DOMContentLoaded', initLanguage);
